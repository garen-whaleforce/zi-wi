/**
 * 取得星曜描述
 */
export function getStarDescription(starName: string): string | null {
  // 移除可能的化曜後綴
  const baseName = starName.replace(/[化祿權科忌]$/, '');

  // 檢查是否有四化
  if (starName.endsWith('化祿')) {
    return `${STAR_DESCRIPTIONS[baseName] || ''}（化祿：${STAR_DESCRIPTIONS['化祿']}）`;
  }
  if (starName.endsWith('化權')) {
    return `${STAR_DESCRIPTIONS[baseName] || ''}（化權：${STAR_DESCRIPTIONS['化權']}）`;
  }
  if (starName.endsWith('化科')) {
    return `${STAR_DESCRIPTIONS[baseName] || ''}（化科：${STAR_DESCRIPTIONS['化科']}）`;
  }
  if (starName.endsWith('化忌')) {
    return `${STAR_DESCRIPTIONS[baseName] || ''}（化忌：${STAR_DESCRIPTIONS['化忌']}）`;
  }

  return STAR_DESCRIPTIONS[starName] || null;
}

/**
 * 取得宮位描述
 */
export function getPalaceDescription(palaceName: string): string | null {
  // 處理可能的「宮」後綴
  const baseName = palaceName.replace(/宮$/, '');
  return PALACE_DESCRIPTIONS[baseName] || null;
}
